# Operadores de ASIGNACIÓN

a = 0      #variable inicial
print (a)

a += 5      #suma en asignación
print (a)

a -= 2      #resta en asignación
print (a)

a *= 6      #multipliacaion en asignación
print (a)

a /= 2      #division en asignación
print (a)

a **= 2      #potencia en asignación
print (a)

a %= 81      #modulo de la división en asignación
print (a)