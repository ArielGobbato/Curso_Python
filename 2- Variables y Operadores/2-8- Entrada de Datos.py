# Entrada de datos

# datos tipo texto
nombre = input("ingrese su nombre:")

print(f"hola {nombre}")

# datos tipo numericos ENTEROS
numero = int(input("ingrese su numero:"))

print(f"su numero ENTRERO es: {numero+10}")

# datos tipo numericos FLOTANTES (DECIMALES)
numero = float(input("ingrese su numero:"))

print(f"su numero FLOTANTE DECIMAL es: {numero+10}")
